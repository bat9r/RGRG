	file.write('\\documentclass[a4paper,12pt]{article} \n')
	file.write('\\usepackage[utf8]{inputenc} \n')
	file.write('\\usepackage[english,ukrainian]{babel} \n')
	file.write('\\usepackage[T2A]{fontenc} \n')
